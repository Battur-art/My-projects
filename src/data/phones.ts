// Backfill optional fields for consistency across the catalog
// - imagesByColor: if missing and colors are defined (not TBA), map each color to the main image as a fallback
// - priceByStorage: if missing and storage options are defined (not TBA),
//   use base price for the first tier, then +10%, +20%, ... for subsequent tiers
phones.forEach((p) => {
  if (!p.imagesByColor && p.colors && p.colors.length > 0 && p.colors[0] !== 'TBA') {
    p.imagesByColor = Object.fromEntries(p.colors.map((c) => [c, p.image]));
  }

  if (!p.priceByStorage && p.storage && p.storage.length > 0 && p.storage[0] !== 'TBA') {
    p.priceByStorage = Object.fromEntries(
      p.storage.map((s, idx) => [s, Math.round(p.price * (1 + idx * 0.1))])
    );
  }
});

export const getPhoneById = (id: string): Phone | undefined => {
  return phones.find(phone => phone.id === id);
};

export const getPhonesByBrand = (brand: 'Apple' | 'Samsung'): Phone[] => {
  return phones.filter(phone => phone.brand === brand);
};

export const getBestSellers = (): Phone[] => {
  return phones.filter(phone => phone.isBestSeller && phone.inStock);
};

export const getNewArrivals = (): Phone[] => {
  return phones.filter(phone => phone.isNew && phone.inStock);
};

export const getFeaturedPhones = (): Phone[] => {
  return phones.filter(phone => (phone.isBestSeller || phone.isNew) && phone.inStock).slice(0, 6);
};